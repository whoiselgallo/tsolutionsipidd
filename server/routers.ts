import { COOKIE_NAME } from "@shared/const";
import { getSessionCookieOptions } from "./_core/cookies";
import { systemRouter } from "./_core/systemRouter";
import { protectedProcedure, publicProcedure, router } from "./_core/trpc";
import { z } from "zod";
import { brandProjectsRouter } from "./routers/brandProjects";
import { brandGenerationRouter } from "./routers/brandGeneration";
import { brandDataRouter } from "./routers/brandData";

export const appRouter = router({
  system: systemRouter,
  auth: router({
    me: publicProcedure.query(opts => opts.ctx.user),
    logout: publicProcedure.mutation(({ ctx }) => {
      const cookieOptions = getSessionCookieOptions(ctx.req);
      ctx.res.clearCookie(COOKIE_NAME, { ...cookieOptions, maxAge: -1 });
      return { success: true } as const;
    }),
  }),
  brandProjects: brandProjectsRouter,
  brandGeneration: brandGenerationRouter,
  brandData: brandDataRouter,
});

export type AppRouter = typeof appRouter;
